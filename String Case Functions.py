# Here are some of the most common case methods you can utilize for strings!

# To show the true power of the code below, I set this string on purpose
s1Word = "hELlo"
print(f"s1Word original: {s1Word}\n")

print(f"s1Word.lower(): {s1Word.lower()}")
print(f"s1Word.upper(): {s1Word.upper()}")
print(f"s1Word.capitalize(): {s1Word.capitalize()}")
print(f"s1Word.title(): {s1Word.title()}")
print(f"s1Word.swapcase(): {s1Word.swapcase()}\n") # <--- \n to separate sections



s2Words = "hELlo WoRLD"
print(f"s2Words original: {s2Words}\n")

print(f"s2Words.lower(): {s2Words.lower()}")
print(f"s2Words.upper(): {s2Words.upper()}")
print(f"s2Words.capitalize(): {s2Words.capitalize()}")
print(f"s2Words.title(): {s2Words.title()}")
print(f"s1Word.swapcase(): {s1Word.swapcase()}\n")



